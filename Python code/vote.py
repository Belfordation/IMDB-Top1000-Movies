import pygal
import pandas as pd
data = pd.read_csv("../IMDB-Movie-Data.csv")

def vote(data):
    vote = data["Votes"].tolist()
    dic = {"0-50000":0, "50001-100000":0, "100001-150000":0, "150001-200000":0, "200001-250000":0,\
    "250001-300000":0, "300001-350000":0, "350001-400000":0, "400000+":0}
    for i in vote:
        if i >= 0 and i < 50000:
            dic["0-50000"] += 1
        if i >= 50001 and i < 100000:
            dic["50001-100000"] += 1
        if i >= 100001 and i < 150000:
            dic["100001-150000"] += 1
        if i >= 150001 and i < 200000:
            dic["150001-200000"] += 1
        if i >= 200001 and i < 250000:
            dic["200001-250000"] += 1
        if i >= 250001 and i < 300000:
            dic["250001-300000"] += 1
        if i >= 300001 and i < 350000:
            dic["300001-350000"] += 1
        if i >= 350001 and i < 400000:
            dic["350001-400000"] += 1
        if i >= 400000:
            dic["400000+"] += 1
    graph = pygal.Bar()
    graph.title = "vote"
    graph.x_labels = ["0-50000", "50001-100000", "100001-150000", "150001-200000", "200001-250000",\
    "250001-300000", "300001-350000", "350001-400000", "400000+"]
    graph.add("", [dic["0-50000"], dic["50001-100000"], dic["100001-150000"], dic["150001-200000"], dic["200001-250000"],\
    dic["250001-300000"], dic["300001-350000"], dic["350001-400000"], dic["400000+"]])
    graph.render_to_file("../Graph/vote.svg")
vote(data)